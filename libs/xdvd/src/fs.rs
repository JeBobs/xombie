use nom::bytes::complete::{take, tag};
use nom::number::complete::{le_u16, le_u32, le_u64, le_u8};

pub const VOLUME_DESCRIPTOR_SECTOR_NUMBER: u64 = 32;

pub const VOLUME_DESCRIPTOR_MAGIC_LEN: usize = 20;

pub const VOLUME_DESCRIPTOR_MAGIC: &[u8;VOLUME_DESCRIPTOR_MAGIC_LEN] =
	b"MICROSOFT*XBOX*MEDIA";

pub const VOLUME_DESCRIPTOR_RESERVED_LEN: usize = 1992;

#[derive(Debug, PartialEq)]
pub struct VolumeDescriptor {
	pub root_directory_sector: u32,
	pub root_directory_length: u32,
	pub timestamp: u64,
}

impl VolumeDescriptor {
	pub fn parse<'a>(input: &'a [u8]) -> nom::IResult<&'a [u8], VolumeDescriptor> {
		let (input, _magic_header) = tag(VOLUME_DESCRIPTOR_MAGIC)(input)?;
		let (input, root_directory_sector) = le_u32(input)?;
		let (input, root_directory_length) = le_u32(input)?;
		let (input, timestamp) = le_u64(input)?;
		let (input, _reserved) =  take(VOLUME_DESCRIPTOR_RESERVED_LEN)(input)?;
		let (input, _magic_footer) = tag(VOLUME_DESCRIPTOR_MAGIC)(input)?;

		Ok((input, VolumeDescriptor {
			root_directory_sector,
			root_directory_length,
			timestamp,
		}))
	}
}

pub const NO_CHILD: u16 = 0x0000;

#[derive(Debug, PartialEq)]
pub struct DirEntry<'a> {
	pub left: u16,
	pub right: u16,
	pub sector: u32,
	pub file_size: u32,
	pub attributes: u8,
	pub name_len: u8,
	pub name: &'a [u8],
}

impl<'a> DirEntry<'a> {
	pub fn parse(input: &'a [u8]) -> nom::IResult<&'a [u8], DirEntry<'a>> {
		let (input, left) = le_u16(input)?;
		let (input, right) = le_u16(input)?;
		let (input, sector) = le_u32(input)?;
		let (input, file_size) = le_u32(input)?;
		let (input, attributes) = le_u8(input)?;
		let (input, name_len) = le_u8(input)?;
		let (input, name) = take(name_len)(input)?;

		Ok((input, DirEntry {
			left,
			right,
			sector,
			file_size,
			attributes,
			name_len,
			name,
		}))
	}
}

